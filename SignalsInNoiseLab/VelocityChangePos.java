public class VelocityChangePos
{
    private int dx;
    private int dy;
    
    public VelocityChangePos(int dx, int dy)
    {
        this.dx = dx;
        this.dy = dy;
    }
    
    public int getDx()
    {
        return this.dx;
    }
    
    public int getDy()
    {
        return this.dy;
    }
}